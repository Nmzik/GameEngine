VulkanRenderer::VulkanRenderer(NativeWindow* window)
    : nativeWindow(window)
{
    nativeWindow->initializeContext();

    createInstance();
    createSurface();
    pickPhysicalDevice();
    createLogicalDevice();
    createSwapChain();
    createImageViews();
    createRenderPass();
    createGraphicsPipeline();

    createFramebuffers();
    createCommandPool();
    createCommandBuffers();
    createSyncObjects();

    for (int i = 0; i < gpuBufferSize; i++)
    {
        vertexBufferIDs.push(i);
        indexBufferIDs.push(i);
        texturesIDs.push(i);
    }

    createStagingBuffer();
}

